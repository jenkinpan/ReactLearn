// ! sort方法是对数组进行排序，它会改变原数组，返回排序后的数组
/*sort方法接受一个函数作为参数，这个函数有两个参数，分别是数组的两个元素
 如果返回值小于0，那么第一个参数会排在第二个参数前面
 如果返回值大于0，那么第一个参数会排在第二个参数后面
 如果返回值等于0，那么两个参数的位置不变
 如果不传参数，那么默认按照字符串的Unicode编码进行排序
 如果想按照数字大小进行排序，那么可以使用a - b
 如果想按照数字大小进行降序排序，那么可以使用b - a
 如果想按照字符串长度进行排序，那么可以使用a.length - b.length
 如果想按照字符串长度进行降序排序，那么可以使用b.length - a.length */
const arr = [3, 7, 19, 6];
const sorted = arr.slice().sort((a, b) => a - b);
// * 只是单纯地使用sort方法，那么会改变原数组。
// * 为了避免这种情况，可以使用slice方法创建一个副本，然后对副本进行排序
